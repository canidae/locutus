	public void updateTable() {
		try {
			ResultSet rs = Database.getArtists(Locutus.getFilter());

			if (rs == null)
				return;

			DefaultTableModel dtb = (DefaultTableModel) jTable1.getModel();
			dtb.setRowCount(0);
			while (rs.next()) {
				dtb.addRow(new Object[]{rs.getInt("artist_id"), rs.getString("name"), rs.getString("sortname"), rs.getString("mbid")});
			}
		} catch (SQLException e) {
			e.printStackTrace();
		}
	}

